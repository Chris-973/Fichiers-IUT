// Création des Tables

CREATE TABLE Continent
(
 name VARCHAR(50),
 area DECIMAL(12,2),
 CONSTRAINT pk_continent PRIMARY KEY (name),
 CONSTRAINT ck_continent_area CHECK (area > 0)
);

CREATE TABLE Region
(
 code CHAR(3),
 name VARCHAR(50),
 rcode CHAR(3),
 cname VARCHAR(50),
 CONSTRAINT pk_region PRIMARY KEY (code),
 CONSTRAINT uk_region_name UNIQUE (name),
 CONSTRAINT fk_region_rcode FOREIGN KEY (rcode) REFERENCES Region (code),
 CONSTRAINT fk_region_cname FOREIGN KEY (cname) REFERENCES Continent (name)
);

CREATE TABLE Country
(
 iso3 CHAR(3),
 iso2 CHAR(2),
 name VARCHAR(50),
 capital VARCHAR(50),
 population DECIMAL,
 area DECIMAL(12,2),
 expectancy DECIMAL(4,2),
 rcode CHAR(3),
 cname VARCHAR(30),
 CONSTRAINT pk_country PRIMARY KEY (iso3),
 CONSTRAINT uk_country_iso2 UNIQUE (iso2),
 CONSTRAINT uk_country_name UNIQUE (name),
 CONSTRAINT fk_country_rcode FOREIGN KEY (rcode) REFERENCES Region (code),
 CONSTRAINT fk_country_cname FOREIGN KEY (cname) REFERENCES Continent(name),
 CONSTRAINT ck_country_population CHECK (population >= 0),
 CONSTRAINT ck_country_area       CHECK (area > 0),
 CONSTRAINT ck_country_expectancy CHECK (expectancy > 0)
);

CREATE TABLE Border
(
 country  CHAR(3),
 bcountry CHAR(3),
 length DECIMAL(10,2),
 CONSTRAINT pk_border PRIMARY KEY (country, bcountry),
 CONSTRAINT fk_border_country  FOREIGN KEY (country)  REFERENCES Country (iso3),
 CONSTRAINT fk_border_bcountry FOREIGN KEY (bcountry) REFERENCES Country (iso3),
 CONSTRAINT ck_border_length CHECK (length > 0)
);

CREATE TABLE Pandemic
(
 iso3 CHAR(3),
 ddate DATE,
 cases DECIMAL,
 deaths DECIMAL,
 rrate DECIMAL,
 icu DECIMAL,
 hpatients DECIMAL,
 tests DECIMAL,
 prate DECIMAL(4,3),
 vacc DECIMAL,
 fvacc DECIMAL,
 CONSTRAINT pk_pandemic PRIMARY KEY (iso3, ddate),
 CONSTRAINT fk_pandemic_iso3 FOREIGN KEY (iso3) REFERENCES Country (iso3),
 CONSTRAINT ck_pandemic_cases       CHECK (cases >= 0),
 CONSTRAINT ck_pandemic_deaths      CHECK (deaths >= 0),
 CONSTRAINT ck_pandemic_rrate       CHECK (rrate >= 0),
 CONSTRAINT ck_pandemic_icu         CHECK (icu >= 0),
 CONSTRAINT ck_pandemic_hpatients   CHECK (hpatients >= 0),
 CONSTRAINT ck_pandemic_tests       CHECK (tests >= 0),
 CONSTRAINT ck_pandemic_prate       CHECK (prate >= 0),
 CONSTRAINT ck_pandemic_vacc        CHECK (vacc >= 0),
 CONSTRAINT ck_pandemic_fvacc       CHECK (fvacc >= 0)
);



